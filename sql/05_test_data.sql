SELECT
    'Книга №' || gs,
    CASE
        WHEN gs % 5 = 0 THEN 'Историческое исследование о развитии общества, государства и культуры.'
        WHEN gs % 5 = 1 THEN 'Фантастическое произведение о космосе, путешествиях и будущих технологиях.'
        WHEN gs % 5 = 2 THEN 'Учебное пособие по алгоритмам, структурам данных и проектированию программ.'
        WHEN gs % 5 = 3 THEN 'Популярная книга по психологии мышления, памяти и принятию решений.'
        ELSE 'Литературное произведение о человеческих отношениях, выборе и жизненном пути.'
        END,
    '978-5-99-' || lpad(gs::text, 7, '0'),
    2000 + (gs % 25),
    1 + (gs % 4),
    1 + (gs % 5)
FROM generate_series(100, 5000) gs
ON CONFLICT (isbn) DO NOTHING;

